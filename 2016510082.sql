--create tables

Create table university(
"university_id" serial NOT NULL,
"name" varchar(100) NOT NULL,
"address" varchar(200),
"email_adress" varchar(200),
"city" varchar(200),
"university_type" varchar(100),
"year_of_foundation" int,
Constraint "pk_university_id" primary key(university_id),
Constraint  "un_name" unique(name)
);
Create table faculty(
"faculty_id" serial NOT NULL,
"university_id" int NOT NULL,
"name" varchar(200) NOT NULL,
"email_address" varchar(200),
Constraint "pk_faculty_id" primary key("faculty_id"),
Constraint "for_key_university_id" foreign key("university_id")
references university("university_id") match simple
on update cascade on delete cascade
);
Create table department(
"department_id" serial NOT NULL,
"faculty_id" int NOT NULL,
"name" varchar(200) NOT NULL,
"email_address" varchar(200),
"language" varchar(100),
"education_type" varchar(100),
"quota" int NOT NULL,
"quoto_for_top_ranked_students" int,
"education_period" int,
"minimum_score" float,
"minimum_order" float,
Constraint "pk_department_id" primary key("department_id"),
Constraint "for_key_faculty_id" foreign key("faculty_id")
references faculty("faculty_id") match simple
on update cascade on delete cascade
);
CREATE TABLE student
(
"student_id" serial NOT NULL,
"name" varchar(200) NOT NULL,
"surname" varchar(200) NOT NULL,
"score" float,
"rank" int,
"position_in_the_high_school"  varchar(200),
Constraint "student_id" primary key("student_id")
);
Create table preferences(
"student_id" int NOT NULL,
"department_id" int NOT NULL,
"order_of_preference" int NOT NULL,
Constraint "pk_preferences" primary key("student_id","department_id"),
Constraint "for_key_student_id" foreign key("student_id")
references student("student_id") match simple
on update cascade on delete cascade,
Constraint "for_key_department_id" foreign key("department_id")
references department("department_id") match simple
on update cascade on delete cascade,
unique(student_id,order_of_preference)
);

